#include "../include/so_long.h"

void	print_map(t_s *s)
{
	ft_printf("---CURRENT MAP---\n");
	s->i = 0;
	while (s->i < s->map_height)
	{
		ft_printf("| %s |\n", s->map[s->i]);
		s->i++;
	}
	ft_printf("-----------------\n");
}
